drop policy if exists "Admin Manage" on storage.objects;

-- อนุญาตทุกคนดูได้ (public read)
create policy "Public Read product-images" on storage.objects 
  for select using (bucket_id = 'product-images');

-- อนุญาต Admin upload เท่านั้น (ป้องกัน user upload ไฟล์แปลกๆ)
create policy "Admin Only Upload" on storage.objects 
  for insert with check (
    bucket_id = 'product-images'
    and (select exists (select 1 from public.profiles where id = auth.uid() and role = 'admin'))
    -- จำกัดประเภทไฟล์: image หรือ video เท่านั้น
    and (
      lower(storage.filename(name)) ~ '\.(jpg|jpeg|png|webp|gif|mp4|mov|webm)$'
    )
  );

-- Admin ลบไฟล์ได้
create policy "Admin Only Delete" on storage.objects 
  for delete using (
    bucket_id = 'product-images'
    and (select exists (select 1 from public.profiles where id = auth.uid() and role = 'admin'))
  );

-- User upload สลิปได้เฉพาะในโฟลเดอร์ slips/
drop policy if exists "User upload slips" on storage.objects;
create policy "User upload payment slips" on storage.objects 
  for insert with check (
    bucket_id = 'payment-slips'
    and auth.uid() is not null
    and lower(storage.filename(name)) ~ '\.(jpg|jpeg|png|webp)$' -- รูปภาพเท่านั้น
    and (select exists (select 1 from public.profiles where id = auth.uid())) -- ต้องเป็น member
  );

create policy "Public read payment slips" on storage.objects 
  for select using (bucket_id = 'payment-slips');

