-- SECTION 4: 📋 AUDIT LOG TABLE
-- ============================================================

create table if not exists public.audit_logs (
  id bigint generated by default as identity primary key,
  actor_id uuid references auth.users on delete set null,
  actor_role text,
  action text not null, -- 'approve_topup', 'reject_order', 'update_balance' etc.
  target_table text,
  target_id bigint,
  old_data jsonb,
  new_data jsonb,
  ip_hash text,
  created_at timestamp with time zone default now() not null
);

alter table public.audit_logs enable row level security;
create policy "Admins view audit logs" on public.audit_logs for select using (public.is_admin());
create policy "No direct insert audit logs" on public.audit_logs for insert with check (false);

create index if not exists idx_audit_logs_actor on public.audit_logs(actor_id);
create index if not exists idx_audit_logs_action on public.audit_logs(action);
create index if not exists idx_audit_logs_created on public.audit_logs(created_at desc);

-- ฟังก์ชัน Log action ภายใน
create or replace function private_log_audit(
  p_action text,
  p_table text,
  p_id bigint,
  p_old jsonb default null,
  p_new jsonb default null
) returns void as $$
begin
  insert into public.audit_logs (actor_id, actor_role, action, target_table, target_id, old_data, new_data)
  values (
    auth.uid(),
    (select role from public.profiles where id = auth.uid()),
    p_action,
    p_table,
    p_id,
    p_old,
    p_new
  );
end;
$$ language plpgsql security definer;

