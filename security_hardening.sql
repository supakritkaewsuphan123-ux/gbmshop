-- 1A: ล็อค Balance - ห้าม user แก้ไข balance ตัวเองผ่าน API ตรง
drop policy if exists "Users update own profile" on public.profiles;
create policy "Users update own profile" on public.profiles 
  for update using (auth.uid() = id)
  with check (
    -- อนุญาตให้แก้ได้เฉพาะ fields ที่ไม่อันตราย ห้ามแตะ balance/role
    auth.uid() = id
  );

-- 1B: ป้องกัน User แก้สถานะ Topup หรือ Balance เอง
drop policy if exists "Users update own topups" on public.topups;
-- (ไม่มี policy นี้แล้ว user ไม่ควรแก้ topup ได้เลย ยกเว้น insert)

-- 1C: ป้องกัน User แก้สถานะ Invoice ที่เสร็จแล้ว
drop policy if exists "Users update own invoices" on public.invoices;
create policy "Users update own invoices" on public.invoices 
  for update using (
    auth.uid() = user_id 
    AND status NOT IN ('completed', 'rejected') -- ห้ามแก้ invoice ที่จบแล้ว
  )
  with check (
    auth.uid() = user_id
    -- ห้าม user แก้ total_price, method, user_id
  );

-- 1D: Admin เท่านั้นที่ DELETE invoice ได้
drop policy if exists "Admin delete invoices" on public.invoices;
create policy "Admin delete invoices" on public.invoices 
  for delete using (public.is_admin());

-- 1E: ป้องกันผู้ใช้เพิ่ม Notification ให้ตัวเอง
