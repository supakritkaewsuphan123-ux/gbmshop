-- SECTION 3: 🚫 RATE LIMITING (DB-based)
-- ============================================================

create table if not exists public.rate_limits (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users on delete cascade,
  ip_hash text, -- hash ของ IP (ไม่เก็บ IP จริง)
  action text not null, -- 'topup', 'order', 'login'
  created_at timestamp with time zone default now() not null
);

alter table public.rate_limits enable row level security;
create policy "No direct access to rate_limits" on public.rate_limits for all using (false);
create index if not exists idx_rate_limits_user_action on public.rate_limits(user_id, action, created_at);

-- ฟังก์ชันตรวจสอบ Rate Limit
create or replace function public.check_rate_limit(
  p_action text,
  p_max_count int,
  p_window_minutes int
) returns boolean as $$
declare
  v_count int;
begin
  select count(*) into v_count
  from public.rate_limits
  where user_id = auth.uid()
    and action = p_action
    and created_at > now() - (p_window_minutes || ' minutes')::interval;
  
  if v_count >= p_max_count then
    return false; -- Rate limit exceeded
  end if;
  
  -- บันทึก request นี้
  insert into public.rate_limits (user_id, action) values (auth.uid(), p_action);
  return true;
end;
$$ language plpgsql security definer;

-- ล้าง Rate Limit เก่า (ควรตั้ง cron job ลบข้อมูลเก่า)
create or replace function public.cleanup_rate_limits() returns void as $$
begin
  delete from public.rate_limits where created_at < now() - interval '1 hour';
end;
$$ language plpgsql security definer;

