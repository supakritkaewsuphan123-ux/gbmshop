-- 5B: อัปเกรด handle_purchase ให้ใช้ Row Lock + Idempotency
create or replace function public.handle_purchase(
  p_user_id uuid,
  p_item_ids bigint[],
  p_method text,
  p_shipping_name text default null,
  p_shipping_phone text default null,
  p_shipping_address text default null,
  p_meet_date text default null,
  p_meet_time text default null,
  p_meet_location text default null,
  p_meet_note text default null,
  p_idempotency_key uuid default null
) returns json as $$
declare
  v_item record;
  v_total numeric := 0;
  v_invoice_id bigint;
  v_event_id uuid;
  v_existing_id bigint;
begin
  -- ตรวจสอบว่า user ถูกต้อง
  if auth.uid() != p_user_id then raise exception 'Unauthorized user'; end if;

  -- ✅ Idempotency Check: ถ้ากดซ้ำ ส่ง result เดิมกลับ
  if p_idempotency_key is not null then
    select id into v_existing_id from public.invoices where idempotency_key = p_idempotency_key;
    if found then
      return json_build_object('success', true, 'invoice_id', v_existing_id, 'idempotent', true);
    end if;
  end if;

  -- ✅ Rate Limit: ไม่เกิน 5 orders ต่อนาที
  if not public.check_rate_limit('order', 5, 1) then
    raise exception 'Rate limit exceeded: too many orders. Please wait a moment.';
  end if;

  -- ✅ ล็อคสินค้าทีละชิ้น (FOR UPDATE ป้องกัน Race Condition)
  for v_item in 
    select * from public.products where id = any(p_item_ids) for update
  loop
    if v_item.stock <= 0 then
      raise exception 'สินค้า "%" หมดสต็อกแล้ว', v_item.name;
    end if;
    v_total := v_total + v_item.price;
  end loop;

  -- ✅ ตัดเงิน Wallet (ถ้าใช้ wallet)
  if p_method = 'wallet' then
    update public.profiles 
    set balance = balance - v_total
    where id = p_user_id and balance >= v_total
    returning id into v_existing_id;
    
    if not found then
      raise exception 'ยอดเงินใน Wallet ไม่เพียงพอ';
    end if;
  end if;

  -- ✅ ตัดสต็อก
  update public.products set stock = stock - 1
  where id = any(p_item_ids);

  -- ✅ สร้าง Invoice
  v_event_id := gen_random_uuid();
  insert into public.invoices (
    user_id, items, total_price, method, status,
    shipping_name, shipping_phone, shipping_address,
    meet_date, meet_time, meet_location, meet_note,
    idempotency_key
  ) values (
    p_user_id, p_item_ids, v_total,
    p_method,
    case when p_method = 'wallet' then 'pending' else 'pending' end,
    p_shipping_name, p_shipping_phone, p_shipping_address,
    p_meet_date, p_meet_time, p_meet_location, p_meet_note,
    coalesce(p_idempotency_key, gen_random_uuid())
  ) returning id into v_invoice_id;

  -- ✅ Audit Log
  perform private_log_audit('create_order', 'invoices', v_invoice_id, null,
    jsonb_build_object('method', p_method, 'total', v_total, 'items', p_item_ids)
  );

  -- แจ้งเตือนสำเร็จ
  insert into public.notifications (user_id, title, message, type, link, event_id)
  values (p_user_id, '🛒 สั่งซื้อสำเร็จ!', 'ออเดอร์ #' || v_invoice_id || ' กำลังรอการยืนยัน', 'success', '/my-orders', v_event_id);

  return json_build_object('success', true, 'invoice_id', v_invoice_id, 'total', v_total);
end;
$$ language plpgsql security definer;

-- 5C: อัปเกรด process_order_action ให้มี Audit Log + Row Lock
create or replace function public.process_order_action(
  p_invoice_id bigint,
  p_action text,
  p_reason text default null,
  p_tracking text default null,
  p_carrier text default null
) returns json as $$
declare
  v_invoice record;
  v_status text;
  v_event_id uuid;
  v_item_id bigint;
begin
  if not (select public.is_admin()) then raise exception 'Unauthorized'; end if;

  -- ✅ Row Lock ป้องกัน Double Approve
  select * into v_invoice from public.invoices 
  where id = p_invoice_id
  for update; -- Lock row
  
  if not found then return json_build_object('success', false, 'message', 'ไม่พบออเดอร์'); end if;
  
  -- ป้องกัน Replay: ถ้าจบแล้วห้ามทำซ้ำ
  if v_invoice.status in ('completed', 'rejected') then
    return json_build_object('success', false, 'message', 'ออเดอร์นี้ดำเนินการเสร็จแล้ว');
  end if;

  v_event_id := gen_random_uuid();

  if p_action = 'approve' then
    v_status := 'completed';
    update public.invoices set 
      status = v_status, tracking_number = p_tracking,
      shipping_carrier = p_carrier, processed_at = now()
    where id = p_invoice_id;

    insert into public.notifications (user_id, title, message, type, link, event_id)
    values (v_invoice.user_id, '📦 สินค้าของคุณกำลังเดินทาง!', 
      'ออเดอร์ #' || p_invoice_id || ' เริ่มจัดส่งแล้ว (' || coalesce(p_carrier, 'ขนส่ง') || ': ' || coalesce(p_tracking, '-') || ')',
      'success', '/my-orders', v_event_id);

  elsif p_action = 'reject' then
    v_status := 'rejected';
    
    -- คืนเงิน Wallet
    if v_invoice.method = 'wallet' then
      update public.profiles set balance = balance + v_invoice.total_price where id = v_invoice.user_id;
    end if;

    -- คืนสต็อก
    foreach v_item_id in array v_invoice.items loop
      update public.products set stock = stock + 1 where id = v_item_id;
    end loop;

    update public.invoices set 
      status = v_status, rejection_reason = p_reason, processed_at = now()
    where id = p_invoice_id;

    insert into public.notifications (user_id, title, message, type, link, event_id)
    values (v_invoice.user_id, '❌ ออเดอร์ถูกปฏิเสธ', 
      'ออเดอร์ #' || p_invoice_id || ' ถูกยกเลิก: ' || coalesce(p_reason, 'ไม่ระบุเหตุผล') ||
      case when v_invoice.method = 'wallet' then ' (คืนเงิน ฿' || v_invoice.total_price || ' เข้า Wallet แล้ว)' else '' end,
      'error', '/my-orders', v_event_id);
  end if;

  -- ✅ Audit Log
  perform private_log_audit(
    p_action || '_order', 'invoices', p_invoice_id,
    jsonb_build_object('status', v_invoice.status),
    jsonb_build_object('status', v_status, 'tracking', p_tracking, 'reason', p_reason)
  );

  return json_build_object('success', true, 'status', v_status);
end;
$$ language plpgsql security definer;

-- 5D: อัปเกรด process_topup_status ให้มี Rate Limit + Audit
create or replace function public.process_topup_status(
  p_topup_id bigint, 
  p_status public.topup_status, 
  p_reason text default null
) returns json as $$
declare
  v_topup record;
  v_event_id uuid;
begin
  if not (select public.is_admin()) then raise exception 'Unauthorized'; end if;

  -- Row Lock ป้องกัน Double Process
  UPDATE public.topups SET status = 'processing' 
  WHERE id = p_topup_id AND status = 'pending' 
  RETURNING * INTO v_topup;
  
  if not found then return json_build_object('success', false, 'message', 'Not found or already processed'); end if;

  v_event_id := gen_random_uuid();

  if p_status = 'completed' then
    UPDATE public.profiles SET balance = balance + v_topup.amount WHERE id = v_topup.user_id;
    UPDATE public.topups SET status = 'completed', processed_at = now() WHERE id = p_topup_id;
    INSERT INTO public.notifications (user_id, title, message, type, link, event_id)
    VALUES (v_topup.user_id, '💰 เติมเงินสำเร็จ!', '฿' || v_topup.amount || ' เข้ากระเป๋าแล้ว', 'success', '/dashboard', v_event_id);
  elsif p_status = 'rejected' then
    UPDATE public.topups SET status = 'rejected', rejection_reason = p_reason, processed_at = now() WHERE id = p_topup_id;
    INSERT INTO public.notifications (user_id, title, message, type, link, event_id)
    VALUES (v_topup.user_id, '❌ ถูกปฏิเสธ', p_reason, 'error', '/dashboard', v_event_id);
  end if;

  -- Audit Log
  perform private_log_audit(
    p_status::text || '_topup', 'topups', p_topup_id,
    jsonb_build_object('status', 'pending', 'amount', v_topup.amount),
    jsonb_build_object('status', p_status::text)
  );

  return json_build_object('success', true);
end;
$$ language plpgsql security definer;

