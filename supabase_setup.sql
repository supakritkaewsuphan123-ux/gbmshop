-- Merge Duplicates
DO $$
DECLARE
    r RECORD;
    v_master_id uuid;
    v_dup_id uuid;
BEGIN
    FOR r IN (SELECT real_email FROM public.profiles WHERE real_email IS NOT NULL GROUP BY real_email HAVING count(*) > 1) LOOP
        SELECT p.id INTO v_master_id FROM public.profiles p LEFT JOIN public.topups t ON t.user_id = p.id WHERE p.real_email = r.real_email ORDER BY (t.id IS NOT NULL) DESC, p.created_at ASC LIMIT 1;
        FOR v_dup_id IN (SELECT id FROM public.profiles WHERE real_email = r.real_email AND id != v_master_id) LOOP
            UPDATE public.topups SET user_id = v_master_id WHERE user_id = v_dup_id;
            UPDATE public.invoices SET user_id = v_master_id WHERE user_id = v_dup_id;
            DELETE FROM public.profiles WHERE id = v_dup_id;
        END LOOP;
    END LOOP;
END $$;

