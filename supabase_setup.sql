-- 7. ระบบการสั่งซื้ออัจฉริยะ (Atomic Purchase RPC)
-- ฟังก์ชันนี้จะจัดการตัดสต็อกและตัดเงินใน Wallet ในขั้นตอนเดียวเพื่อความปลอดภัย
create or replace function handle_purchase(
  p_user_id uuid,
  p_item_ids bigint[],
  p_method text,
  p_shipping_name text default null,
  p_shipping_phone text default null,
  p_shipping_address text default null,
  p_meet_date text default null,
  p_meet_time text default null,
  p_meet_location text default null,
  p_meet_note text default null
) returns json as $$
declare
  v_total_price numeric := 0;
  v_user_balance numeric := 0;
  v_product_id bigint;
  v_invoice_id bigint;
  v_item record;
  v_status text := 'pending';
begin
  -- 1. ตรวจสอบสต็อกสินค้าทุกชิ้นก่อน
  foreach v_product_id in array p_item_ids loop
    if not exists (select 1 from public.products where id = v_product_id and stock > 0) then
      raise exception 'สินค้าบางรายการหมดสต็อกแล้ว กรุณาตรวจสอบอีกครั้ง';
    end if;
  end loop;

  -- 2. คำนวณราคารวมทั้งหมด
  select sum(price) into v_total_price 
  from public.products 
  where id = any(p_item_ids);

  -- 3. หากชำระผ่าน Wallet ให้เช็คยอดเงินและหักเงินทันที
  if p_method = 'wallet' then
    select balance into v_user_balance from public.profiles where id = p_user_id;
    if v_user_balance < v_total_price then
      raise exception 'ยอดเงินใน Wallet ไม่เพียงพอ (ขาดอยู่ ฿%)', (v_total_price - v_user_balance);
    end if;

    -- ตัดเงิน
    update public.profiles 
    set balance = balance - v_total_price 
    where id = p_user_id;
    
    v_status := 'paid'; -- เปลี่ยนสถานะเป็นชำระแล้วกรณีจ่ายผ่าน Wallet
  end if;

  -- 4. หักสต็อกสินค้าทุกชิ้น
  foreach v_product_id in array p_item_ids loop
    update public.products 
    set stock = stock - 1 
    where id = v_product_id;
  end loop;

  -- 5. บันทึกรายการสั่งซื้อ (Invoice)
  insert into public.invoices (
    user_id, items, total_price, method, status, 
    shipping_name, shipping_phone, shipping_address,
    meet_date, meet_time, meet_location, meet_note
  ) values (
    p_user_id, p_item_ids, v_total_price, p_method, v_status,
    p_shipping_name, p_shipping_phone, p_shipping_address,
    p_meet_date, p_meet_time, p_meet_location, p_meet_note
  ) returning id into v_invoice_id;

  -- 6. สร้างแจ้งเตือนให้ผู้ใช้
  insert into public.notifications (user_id, title, message, type, link, event_id)
  values (
    p_user_id, 
    '📦 สั่งซื้อสำเร็จ!', 
    'ออเดอร์ #' || v_invoice_id || ' ของคุณได้รับการยืนยันแล้ว',
    'success',
    '/dashboard',
    gen_random_uuid()
  );

  return json_build_object(
    'success', true, 
    'invoice_id', v_invoice_id, 
    'total_price', v_total_price,
    'status', v_status
  );

exception when others then
  return json_build_object('success', false, 'message', sqlerrm);
end;
$$ language plpgsql security definer;

-- 8. ระบบจัดการคำสั่งซื้ออัจฉริยะ 2.0 (Order Action RPC)
-- ฟังก์ชันนี้จัดการการอนุมัติ (พร้อมเลขพัสดุ) และการปฏิเสธ (พร้อมคืนเงิน)
create or replace function process_order_action(
  p_invoice_id bigint,
  p_action text, -- 'approve' หรือ 'reject'
  p_reason text default null,
  p_tracking text default null,
  p_carrier text default null
) returns json as $$
declare
  v_invoice record;
  v_status text;
  v_event_id uuid;
begin
  -- 1. ตรวจสอบแอดมิน
  if not (select public.is_admin()) then raise exception 'Unauthorized'; end if;

  -- 2. ดึงข้อมูลออเดอร์
  select * into v_invoice from public.invoices where id = p_invoice_id;
  if not found then return json_build_object('success', false, 'message', 'ไม่พบออเดอร์'); end if;

  v_event_id := gen_random_uuid();

  -- 3. กรณีอนุมัติ (Approve)
  if p_action = 'approve' then
    v_status := 'completed';
    update public.invoices set 
      status = v_status,
      tracking_number = p_tracking,
      shipping_carrier = p_carrier,
      processed_at = now()
    where id = p_invoice_id;

    insert into public.notifications (user_id, title, message, type, link, event_id)
    values (
      v_invoice.user_id, 
      '📦 สินค้าของคุณกำลังเดินทาง!', 
      'ออเดอร์ #' || p_invoice_id || ' เริ่มจัดส่งแล้ว (' || coalesce(p_carrier, 'ขนส่ง') || ': ' || coalesce(p_tracking, '-') || ')',
      'success', '/dashboard', v_event_id
    );

  -- 4. กรณีปฏิเสธ (Reject)
  elsif p_action = 'reject' then
    v_status := 'rejected';
    
    -- คืนเงินเข้า Wallet หากชำระผ่าน Wallet
    if v_invoice.method = 'wallet' then
      update public.profiles 
      set balance = balance + v_invoice.total_price 
      where id = v_invoice.user_id;
    end if;

    -- คืนสต็อกสินค้า
    declare
      v_item_id bigint;
    begin
      foreach v_item_id in array v_invoice.items loop
        update public.products set stock = stock + 1 where id = v_item_id;
      end loop;
    end;

    update public.invoices set 
      status = v_status,
      rejection_reason = p_reason,
      processed_at = now()
    where id = p_invoice_id;

    insert into public.notifications (user_id, title, message, type, link, event_id)
    values (
      v_invoice.user_id, 
      '❌ ออเดอร์ถูกปฏิเสธ', 
      'ออเดอร์ #' || p_invoice_id || ' ถูกยกเลิก: ' || coalesce(p_reason, 'ไม่ระบุเหตุผล') || ' (คืนเงินเข้า Wallet แล้ว)',
      'error', '/dashboard', v_event_id
    );
  end if;

  return json_build_object('success', true, 'status', v_status);
end;
$$ language plpgsql security definer;

