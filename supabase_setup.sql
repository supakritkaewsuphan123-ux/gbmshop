-- Process Topup Logic
create or replace function public.process_topup_status(p_topup_id bigint, p_status public.topup_status, p_reason text default null)
returns json as $$
declare
    v_admin_id uuid;
    v_topup record;
    v_event_id uuid;
begin
    v_admin_id := auth.uid();
    if not (select public.is_admin()) then raise exception 'Unauthorized'; end if;

    UPDATE public.topups SET status = 'processing' WHERE id = p_topup_id AND status = 'pending' RETURNING * INTO v_topup;
    if not found then return json_build_object('success', false, 'message', 'Not found'); end if;

    v_event_id := gen_random_uuid();

    if p_status = 'completed' then
        UPDATE public.profiles SET balance = balance + v_topup.amount WHERE id = v_topup.user_id;
        UPDATE public.topups SET status = 'completed', processed_at = now() WHERE id = p_topup_id;
        INSERT INTO public.notifications (user_id, title, message, type, link, event_id)
        VALUES (v_topup.user_id, '💰 เติมเงินสำเร็จ!', '฿' || v_topup.amount || ' เข้ากระเป๋าแล้ว', 'success', '/dashboard', v_event_id);
    elsif p_status = 'rejected' then
        UPDATE public.topups SET status = 'rejected', rejection_reason = p_reason, processed_at = now() WHERE id = p_topup_id;
        INSERT INTO public.notifications (user_id, title, message, type, link, event_id)
        VALUES (v_topup.user_id, '❌ ถูกปฏิเสธ', p_reason, 'error', '/dashboard', v_event_id);
    end if;
    return json_build_object('success', true);
end;
$$ language plpgsql security definer;

-- Smart Login Resolver
create or replace function public.resolve_auth_email(p_identifier text)
returns text as $$
declare
    v_email text;
begin
    select u.email into v_email from auth.users u join public.profiles p on p.id = u.id left join public.topups t on t.user_id = p.id
    where lower(p.username) = lower(trim(p_identifier)) order by (t.id is not null) desc, p.created_at asc limit 1;
    if v_email is not null then return v_email; end if;

    select u.email into v_email from auth.users u join public.profiles p on p.id = u.id left join public.topups t on t.user_id = p.id
    where lower(p.real_email) = lower(trim(p_identifier)) order by (t.id is not null) desc, p.created_at asc limit 1;
    return COALESCE(v_email, p_identifier);
end;
$$ language plpgsql security definer;

